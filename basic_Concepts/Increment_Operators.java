package basic_Concepts;

public class Increment_Operators {
		public static void main(String[] args) {
			
			double apple = 5; // decimal variable
			
			
			apple += 8; // same as apple = apple + 8
			System.out.println(apple);
			
			apple -= 8;	// same as apple = apple - 8		
			System.out.println(apple);

			apple *= 8; // same as apple = apple * 8
			System.out.println(apple);
			
			
			
		}
}
