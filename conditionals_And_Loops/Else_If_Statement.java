package conditionals_And_Loops;

public class Else_If_Statement {

	public static void main(String[] args) {
		
			boolean apples = true ;
			
			if (apples == true) { 
				System.out.println("you have apples");
			
			} else if (apples == false) {
				System.out.println("You dont have apples");
			} else { 
				System.out.println("i dont know what fruit you have");
			}	
			

	}

}
                                                     